const SECTION_EMOJIS = ['📌','📊','📋','☁️','🎛','➕','🧠','⌨️','❓','🤖','📋'];
const TABLE_HEADERS  = ['Шаг','Кнопка','Метрика','Вопрос','Время','Команда','Раздел','Файл','Данные','Принцип','Твой вопрос','Как спросить','Ограничение','Зона','Ситуация','Что показывает'];
const DIALOG_MARKERS = ['── ДИАЛОГ', 'Ты:', 'Агент:'];

async function updateSheet(auth) {
  const sheets = google.sheets({ version: 'v4', auth });

  const meta  = await sheets.spreadsheets.get({ spreadsheetId: SHEET_ID });
  const sheet = meta.data.sheets.find(s => s.properties.title === SHEET_NAME);
  if (!sheet) { console.error(`❌ Лист "${SHEET_NAME}" не найден`); return; }
  const sheetId = sheet.properties.sheetId;

  // Clear
  await sheets.spreadsheets.values.clear({
    spreadsheetId: SHEET_ID,
    range: `'${SHEET_NAME}'!A1:Z300`,
  });

  // Write values
  await sheets.spreadsheets.values.update({
    spreadsheetId: SHEET_ID,
    range: `'${SHEET_NAME}'!A1`,
    valueInputOption: 'RAW',
    requestBody: { values: ROWS },
  });

  const requests = [
    // Title
    {
      repeatCell: {
        range: { sheetId, startRowIndex: 0, endRowIndex: 1 },
        cell: { userEnteredFormat: {
          textFormat: { bold: true, fontSize: 16, foregroundColor: { red: 1, green: 1, blue: 1 } },
          backgroundColor: { red: 0.08, green: 0.12, blue: 0.22 },
        }},
        fields: 'userEnteredFormat(textFormat,backgroundColor)',
      }
    },

    // Section headers (РАЗДЕЛ N или emoji-заголовки)
    ...ROWS.map((row, i) => {
      const t = String(row[0] || '');
      const isSection = t.includes('РАЗДЕЛ') || SECTION_EMOJIS.some(e => t.startsWith(e));
      if (!isSection) return null;
      return {
        repeatCell: {
          range: { sheetId, startRowIndex: i, endRowIndex: i + 1 },
          cell: { userEnteredFormat: {
            textFormat: { bold: true, fontSize: 11, foregroundColor: { red: 1, green: 1, blue: 1 } },
            backgroundColor: { red: 0.16, green: 0.5, blue: 0.73 },
          }},
          fields: 'userEnteredFormat(textFormat,backgroundColor)',
        }
      };
    }).filter(Boolean),

    // Sub-section headers (📍)
    ...ROWS.map((row, i) => {
      const t = String(row[0] || '');
      if (!t.startsWith('📍')) return null;
      return {
        repeatCell: {
          range: { sheetId, startRowIndex: i, endRowIndex: i + 1 },
          cell: { userEnteredFormat: {
            textFormat: { bold: true, fontSize: 10, foregroundColor: { red: 1, green: 1, blue: 1 } },
            backgroundColor: { red: 0.25, green: 0.35, blue: 0.55 },
          }},
          fields: 'userEnteredFormat(textFormat,backgroundColor)',
        }
      };
    }).filter(Boolean),

    // Dialog markers (── ДИАЛОГ)
    ...ROWS.map((row, i) => {
      const t = String(row[0] || '');
      if (!t.startsWith('──')) return null;
      return {
        repeatCell: {
          range: { sheetId, startRowIndex: i, endRowIndex: i + 1 },
          cell: { userEnteredFormat: {
            textFormat: { bold: true, italic: true },
            backgroundColor: { red: 0.95, green: 0.95, blue: 0.98 },
          }},
          fields: 'userEnteredFormat(textFormat,backgroundColor)',
        }
      };
    }).filter(Boolean),

    // "Ты:" rows — light green
    ...ROWS.map((row, i) => {
      if (String(row[0] || '') !== 'Ты:') return null;
      return {
        repeatCell: {
          range: { sheetId, startRowIndex: i, endRowIndex: i + 1 },
          cell: { userEnteredFormat: {
            textFormat: { bold: true, foregroundColor: { red: 0.1, green: 0.4, blue: 0.1 } },
            backgroundColor: { red: 0.9, green: 0.98, blue: 0.9 },
          }},
          fields: 'userEnteredFormat(textFormat,backgroundColor)',
        }
      };
    }).filter(Boolean),

    // "Агент:" rows — light blue
    ...ROWS.map((row, i) => {
      if (String(row[0] || '') !== 'Агент:') return null;
      return {
        repeatCell: {
          range: { sheetId, startRowIndex: i, endRowIndex: i + 1 },
          cell: { userEnteredFormat: {
            textFormat: { bold: true, foregroundColor: { red: 0.1, green: 0.2, blue: 0.6 } },
            backgroundColor: { red: 0.9, green: 0.94, blue: 1.0 },
          }},
          fields: 'userEnteredFormat(textFormat,backgroundColor)',
        }
      };
    }).filter(Boolean),

    // Table headers
    ...ROWS.map((row, i) => {
      const t = String(row[0] || '');
      if (!TABLE_HEADERS.includes(t)) return null;
      return {
        repeatCell: {
          range: { sheetId, startRowIndex: i, endRowIndex: i + 1 },
          cell: { userEnteredFormat: {
            textFormat: { bold: true },
            backgroundColor: { red: 0.86, green: 0.92, blue: 1.0 },
          }},
          fields: 'userEnteredFormat(textFormat,backgroundColor)',
        }
      };
    }).filter(Boolean),

    // Column widths
    { updateDimensionProperties: { range: { sheetId, dimension: 'COLUMNS', startIndex: 0, endIndex: 1 }, properties: { pixelSize: 280 }, fields: 'pixelSize' }},
    { updateDimensionProperties: { range: { sheetId, dimension: 'COLUMNS', startIndex: 1, endIndex: 2 }, properties: { pixelSize: 420 }, fields: 'pixelSize' }},
    { updateDimensionProperties: { range: { sheetId, dimension: 'COLUMNS', startIndex: 2, endIndex: 3 }, properties: { pixelSize: 200 }, fields: 'pixelSize' }},
    { updateDimensionProperties: { range: { sheetId, dimension: 'COLUMNS', startIndex: 3, endIndex: 4 }, properties: { pixelSize: 150 }, fields: 'pixelSize' }},
    { updateDimensionProperties: { range: { sheetId, dimension: 'COLUMNS', startIndex: 4, endIndex: 5 }, properties: { pixelSize: 200 }, fields: 'pixelSize' }},

    // Wrap + top-align all
    {
      repeatCell: {
        range: { sheetId, startRowIndex: 0, endRowIndex: ROWS.length },
        cell: { userEnteredFormat: { wrapStrategy: 'WRAP', verticalAlignment: 'TOP' }},
        fields: 'userEnteredFormat(wrapStrategy,verticalAlignment)',
      }
    },

    // Freeze row 1
    { updateSheetProperties: {
      properties: { sheetId, gridProperties: { frozenRowCount: 1 }},
      fields: 'gridProperties.frozenRowCount',
    }},

    // Row groups (раскрывающиеся блоки — нажми «−» слева чтобы свернуть)
    ...GROUPS.map(g => ({
      addDimensionGroup: {
        range: { sheetId, dimension: 'ROWS', startIndex: g.start, endIndex: g.end },
      }
    })),
  ];

  await sheets.spreadsheets.batchUpdate({
    spreadsheetId: SHEET_ID,
    requestBody: { requests },
  });

  console.log(`✅ Лист "${SHEET_NAME}" обновлён! ${ROWS.length} строк, ${GROUPS.length} раскрывающихся групп`);
}

(async () => {
  try {
    const auth = await getAuth();
    await updateSheet(auth);
    console.log(`\n📊 https://docs.google.com/spreadsheets/d/${SHEET_ID}`);
  } catch (e) {
    console.error('❌ Ошибка:', e.message);
    if (e.errors) e.errors.forEach(err => console.error(' ', err.message));
  }
})();
